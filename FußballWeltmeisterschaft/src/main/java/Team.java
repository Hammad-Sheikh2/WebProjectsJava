//This is Team Class
public class Team {
    //This is Team Class Data Members
    public String Name;
    public int W,D,L,MP,Pts;
    public String Logo;
    public int goalsPoints;
    public Team(String name,String logo){
        Name=name;
        W=0;
        D=0;
        L=0;
        Pts=0;
        MP=0;
        Logo=logo;
        goalsPoints=0;
    }
}
